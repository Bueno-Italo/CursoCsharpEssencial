//Usando operadores atrubuição com tipos numéricos
var x = 10;

Console.WriteLine($"Valor inicial de x = {x}");
Console.WriteLine($"x+=5 == {x += 5}");
Console.WriteLine($"-+=5 == {x -= 5}");
Console.WriteLine($"*+=5 == {x *= 5}");
Console.WriteLine($"/+=5 == {x /= 5}");
Console.WriteLine($"%+=5 == {x %= 5}");
